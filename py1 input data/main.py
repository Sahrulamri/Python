# input user
# data yang dimasukkan pasti string default
data = input('masukkan nama : ')
print('nama : ', data, 'type : ', type(data))

# data int
dataInt = int(input('masukkan umur :'))
print('umur : ', dataInt, 'type', type(dataInt))

dataFloat = float(input('masukkan tinggi badan :'))
print('tinggi badan : ', dataFloat, 'type', type(dataFloat))

# Boolean
bool = bool(int(input('mahasiswa (1 jika iya/ 0 jika tidak) :')))
