# Operasi aritmatika

a = 7
b = 5
# perkalian
hasil = a * b
print('hasil dari ', a, ' x ', b, ' adalah ', hasil)

# pertambahan
hasil = a + b
print('hasil dari ', a, ' + ', b, ' adalah ', hasil)

# pembagian
hasil = a / b
print('hasil dari ', a, ' / ', b, ' adalah ', hasil)

# pengurangan
hasil = a - b
print('hasil dari ', a, ' - ', b, ' adalah ', hasil)

# modulus
hasil = b % a
print('hasil dari ', b, ' mod ', a, ' adalah ', hasil)

# perpangkatan (eksponen)
hasil = a ** b
print('hasil dari ', a, ' pangkat ', b, ' adalah ', hasil)

# floor division ( pembulatan kebawah )
hasil = a // b
print('hasil dari ', a, ' / ', b, 'lalu dibulatkan kebawah adalah ', hasil)

x = 4
